/*
Operadores de Igualdade

===
Verifica a igualdade de valor e tipo

*/

// 2 números
let num1 = 1;
let num2 = 1;

console.log(num1 === num2); // true

//-------------------------

// 1 string e 1 número
let num3 = '1';
let num4 = 1;

console.log(num3 === num4); //false

//-------------------------

// 1 string e 1 número, mas, os valores são iguais
let num5 = '1';
let num6 = 1;

console.log(num3 == num4); // true
