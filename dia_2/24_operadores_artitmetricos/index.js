/*
Operadores Artimétricos

*/

let num1 = 3;
let num2 = 2;

console.log(num1 + num2);

console.log(num1 - num2);

console.log(num1 * num2);

console.log(num1 / num2);

console.log(num1 % num2);

console.log(num1 ** num2);

let total1 = num1 + num2;

let total2 = num1 + num2;

console.log(total1);

// incremento ++

console.log('Incremento antes: ' + ++total1);

// Altera o valor, mas, não exibe no console
console.log('Incremento depois: ' + total2++);
// Exibe o valor alterado, após o incremento
console.log(total2);

// decremento --

let total3 = num1 + num2;

console.log('Decremento antes: ' + --total3);

let total4 = num1 + num2;

console.log('Decremento depois: ' + total4--);

console.log(total4);
