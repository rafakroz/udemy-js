let num = 3;

let str = 'Rafael'

console.log(num > 0);
console.log(num < 0);
console.log(num >= 0);
console.log(num <= 3);

// idêntico
console.log(num === 3);
console.log('Igualdade: ' + (num === 3));

console.log(num === str);

// não é idêntico
console.log(num !== 3);
console.log('Igualdade: ' + (num !== 3));

console.log(num !== str);